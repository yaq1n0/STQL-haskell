evalP = evalStateT

-- Action to read a token
readToken::P Token
readToken = do
          s <- get  
          case alexScan s 0 of
                AlexEOF -> return TEOF
                AlexError _ -> throwError "Lexical error occurred."
                AlexSkip inp' _ -> do	
                                    put inp'
                                    readToken
                AlexToken inp' _ tk -> do 
                                        put inp'
                                        return tk

-- The lexer function to be passed to Happy
lexer::(Token -> P a)->P a
lexer cont = do
  token <- readToken
  cont token
